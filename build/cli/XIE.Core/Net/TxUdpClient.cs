namespace XIE.Net
{
#if LINUX
	using SOCKET = System.Int32;
#else
	using SOCKET = System.IntPtr;
#endif

	/// <summary>
	/// UDP通信構造体
	/// </summary>
	[Serializable]
	[TypeConverter(typeof(CxSortingConverter))]
	[StructLayout(LayoutKind.Sequential, Pack = XIE.Defs.XIE_PACKING_SIZE)]
	public struct TxUdpClient :
		IEquatable<TxUdpClient>
	{
		#region プロパティ:

		/// <summary>
		/// ソケットハンドル
		/// </summary>
		public SOCKET Socket;

		/// <summary>
		/// IPアドレス
		/// </summary>
		public TxIPAddress IPAddress;

		/// <summary>
		/// ポート番号
		/// </summary>
		public int Port;

		#endregion

		#region 比較:

		/// <summary>
		/// IEquatable の実装: 自身の内容と指定されたオブジェクトの内容を比較します。
		/// </summary>
		/// <param name="other">比較するオブジェクト。</param>
		/// <returns>
		///		内容が等しい場合は true、それ以外は false を返します。
		/// </returns>
		public bool Equals(TxUdpClient other)
		{
			if (this.Socket != other.Socket) return false;
			if (this.IPAddress != other.IPAddress) return false;
			if (this.Port != other.Port) return false;
			return true;
		}

		/// <summary>
		/// 自身の内容と指定されたオブジェクトの内容を比較します。
		/// </summary>
		/// <param name="obj">比較するオブジェクト。</param>
		/// <returns>
		///		内容が等しい場合は true、それ以外は false を返します。
		/// </returns>
		public override bool Equals(object obj)
		{
			if (ReferenceEquals(obj, null)) return false;
			if (!(obj is TxUdpClient)) return false;
			return this.Equals((TxUdpClient)obj);
		}

		/// <summary>
		/// ハッシュ値の取得
		/// </summary>
		/// <returns>
		///		常に 0 を返します。
		/// </returns>
		public override int GetHashCode()
		{
			return 0;
		}

		#endregion

		#region 比較オペレータ:

		/// <summary>
		/// ２つのオブジェクトの内容を比較します。
		/// </summary>
		/// <param name="left">左辺値</param>
		/// <param name="right">右辺値</param>
		/// <returns>
		///		内容が等しい場合は true、それ以外は false を返します。
		/// </returns>
		public static bool operator ==(TxUdpClient left, TxUdpClient right)
		{
			return left.Equals(right);
		}

		/// <summary>
		/// ２つのオブジェクトの内容を比較します。
		/// </summary>
		/// <param name="left">左辺値</param>
		/// <param name="right">右辺値</param>
		/// <returns>
		///		内容が等しい場合は false、それ以外は true を返します。
		/// </returns>
		public static bool operator !=(TxUdpClient left, TxUdpClient right)
		{
			return !left.Equals(right);
		}

		#endregion
	}
}
